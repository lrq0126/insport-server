                    <text style="font-size:30upx;">　　<strong>一.基础税率/WTO税率</strong></text>
                </view>
                <view>
                    <text style="font-size:30upx;"><strong></strong></text>&nbsp;
                </view>
                <view>
                    <text style="font-size:30upx;">　　这里WTO指MFN(最惠国)税率，某国的来自于其最惠国的进口产品享受的关税税率。订有双边或多边贸易协定的国家采用，最惠国待遇是关税和贸易总协定中的一项重要条款，按此条规定，缔约国双方相互之间现在和将来所给予第三国在贸易上的优惠、豁免和特权同样给予缔约对方，体现在关税上，即为最惠国税，这种形式的关税减让是互惠的。现在世界贸易组织成员国之间的贸易都适用最惠国税率，最惠国税率是正常的税率。一般，普惠制以及自贸区的优惠关税是在WTO关税的基础上在进行关税减免的。基础税率：东盟的部分国家的优惠安排中，作为优惠基数核和起点的关税。</text>
                </view>
                <view>
                    <text style="font-size:30upx;"></text>&nbsp;
                </view>
                <view>
                    <text style="font-size:30upx;">　　<strong>二.优惠税率：</strong></text>
                </view>
                <view>
                    <text style="font-size:30upx;"><strong></strong></text>&nbsp;
                </view>
                <view>
                    <text style="font-size:30upx;">　　是指对来自特定受惠国的进口货物征收的低于普通税率的优惠税率关税。优惠关税一般是互惠的，通过国际间的贸易或关税协定，协定双方相互给予优惠关税待遇;但也有单方面的，给惠国给予受惠国单向的优惠关税待遇，不要求反向优惠，如普惠制下的优惠关税;世贸组织实行多边的普遍最惠国优惠关税，任一缔约方给予所有缔约方。</text>
                </view>
                <view>
                    <text style="font-size:30upx;"></text>&nbsp;
                </view>
                <view>
                    <text style="font-size:30upx;">　　目前优惠关税包括两种：</text>
                </view>
                <view>
                    <text style="font-size:30upx;"></text>&nbsp;
                </view>
                <view>
                    <text style="font-size:30upx;">　　1. 普惠制优惠幅度：是发达国家向发展中国家提供的一种优惠税率，它是在最惠国税率的基础上进行减免，因而是最低税率，是单向的，非互惠的税率。包括欧盟、日本、加拿大、挪威、瑞士、澳大利亚等国家。</text>
                </view>
                <view>
                    <text style="font-size:30upx;"></text>&nbsp;
                </view>
                <view>
                    <text style="font-size:30upx;">　　2. 自贸区优惠幅度：是由优惠贸易安排和自由贸易区等形式出现的区域贸易安排，在这些区域内部实行的一种比最惠国税率还要优惠的“优惠制”税率，是世界贸易组织最惠国待遇原则的例外情形之一。包括东盟国家、亚太协定国家、巴基斯坦、智利、秘鲁、新西兰等国家。</text>
                </view>
